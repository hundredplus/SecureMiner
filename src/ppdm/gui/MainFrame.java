private void newClusterMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newClusterMenuItemActionPerformed
    try {
        KMeansGUI kMeansGUI = new KMeansGUI();
        KMeansParty kMeansParty = kMeansGUI.getKMeansParty();
        if (kMeansParty != null) {
            KMeansResultFrame kmrf = new KMeansResultFrame(kMeansParty);
            desktop.add(kmrf);
            kmrf.setVisible(true);
            kmrf.setSelected(true);
        }
    } catch (Exception ex) {
        ErrorController.show(null, ex);
    }
    
}//GEN-LAST:event_newClusterMenuItemActionPerformed

