private void newAssoMenuItemActionPerformed(java.awt.event.ActionEvent evt) {                                                
    AssoGUI aprf = new AssoGUI();
    AssociationParty asso = aprf.getAssoParty();
    if (asso != null){
        AssoResultFrame apf = new AssoResultFrame(asso);
        apf.setTitle(asso.wrapper.projectName + " - Association Rule Mining Project");
        desktop.add(apf);
        apf.setVisible(true);
        try {
                apf.setSelected(true);                                               
        } catch (PropertyVetoException ex) {
            ErrorController.show(null, ex);
        }
    }
}

